<?php
/*-------intdiv() -- Integer division------- */
echo intdiv(100, 3) . "\n";
echo intdiv(5, 2) . "\n";
echo intdiv(-5, -2) . "\n";

/*-------sqrt() -- Square root------- */
echo (sqrt(9) . "\n");
echo (sqrt(4) . "\n");
echo (sqrt(0) . "\n");
echo (sqrt(144) . "\n");
echo (sqrt(5) . "\n");

/*-------pow() -- Exponential expression------- */
echo (pow(2, 4) . "\n");
echo (pow(2, 2) . "\n");
echo (pow(-2, 4) . "\n");
echo (pow(-2, -4) . "\n");
